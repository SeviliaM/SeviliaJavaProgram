package com.java.class09;

public class IncrementOperator {
    public static void main(String[] args) {
        int i = 5;
        i = i + 1; i = i - 1;
        System.out.println(i);
        i++;
        System.out.println(i);
        i--;
        System.out.println(i);

        System.out.println(i);
    }
}
